# This module provides basic arithmetic operations as functions.

def addition(num_1, num_2):
    """
    Adds two numbers together.

    Parameters:
    num_1 (int or float): The first number.
    num_2 (int or float): The second number.

    Returns:
    int or float: The sum of num_1 and num_2.
    """
    add = num_1 + num_2
    return add 

def sub(num_1, num_2):
    """
    Subtracts the second number from the first.

    Parameters:
    num_1 (int or float): The number to be subtracted from.
    num_2 (int or float): The number to subtract.

    Returns:
    int or float: The result of num_1 - num_2.
    """
    sub = num_1 - num_2
    return sub 

def multiply(num_1, num_2):
    """
    Multiplies two numbers.

    Parameters:
    num_1 (int or float): The first number.
    num_2 (int or float): The second number.

    Returns:
    int or float: The product of num_1 and num_2.
    """
    mul = num_1 * num_2
    return mul

